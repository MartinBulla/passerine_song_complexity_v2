#' Many organisms use acoustic signals to attract mates and repel rivals [@eriksson1986a; @ritchie1998a]. Among the most spectacular and enigmatic examples are the elaborate songs of passerine birds (order: Passeriformes). Passerine songs vary from single notes to repertoires with hundreds of distinct elements, exemplified by iconic mockingbirds and nightingales [@weir2011; @snyder2019a]. Numerous hypotheses have been proposed to explain between-species differences in bird song complexity, such as sexual selection, species recognition, cognition, habitat structure, and trade-offs with other signals such as plumage coloration, but empirical support remains mixed [@weir2011; @snyder2019a; @botero2009; @mason2014a; @shutler1990a; @najar2019a; @read1992a; @searcy1992a]. Yet, despite extensive work on avian visual signals and their global evolutionary patterns [@cooney2022; @dale2015], we lack even a basic description of how song complexity distributes across the globe, and thus a comprehensive test of the main competing hypotheses across all passerines. Here, (i) to provide a global map of song complexity and (ii) to test the most influential hypotheses explaining interspecific variability in song complexity, we quantified male song complexity for 4,940 passerine species, representing ~83% of extant species [@jetz2012a], using 18,065 song recordings (Fig. [1](#F_1)) from large citizen-science databases (Xeno-canto https://xeno-canto.org/ and Macaulay Library https://www.macaulaylibrary.org/). We predict strong clade-specific effects because song acquisition differs fundamentally between Suboscines with innate song and song-learning Oscines [@beecher2005a].
