# map function to plot Oscines and Suboscines separately
mapO_S <- function(dat_ = A, v, name = "", qq = c(0.025, 0.975),
                   br = NA, lbls = NA, outlier = FALSE) {

  if (v %in% c("species_richness")) {
    vo <- v
  } else {
    vo <- paste0(v, "_osc")
  }

  xxo <- data.table(dat_)
  xxo <- xxo[, .(get(vo))]
  xo <- dat_[, "geometry"]
  xo$v <- xxo$V1
  xo <- xo[!is.na(xo$v), ]
  xo$clade <- "Oscines"

  if (v %in% c("species_richness")) {
    vs <- v
  } else {
    vs <- paste0(v, "_sub")
  }

  xxs <- data.table(dat_)
  xxs <- xxs[, .(get(vs))]
  xs <- dat_[, "geometry"]
  xs$v <- xxs$V1
  xs <- xs[!is.na(xs$v), ]
