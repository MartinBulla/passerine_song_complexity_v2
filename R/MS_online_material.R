# helper to save/read simulations
sim_or_read <- function(model, file_name, nsim = 5000, generate_new = TRUE,
                        save_dir = here::here("Output", "est_out"), seed = 5) {

  file_path <- file.path(save_dir, paste0(file_name, ".rds"))

  if (!generate_new) {
    if (!file.exists(file_path)) {
      stop("Saved simulation file does not exist: ", file_path,
           "\nRun once with generate_new = TRUE.")
    }
    return(readRDS(file_path))
  }

  set.seed(seed)
  out <- arm::sim(model, n.sim = nsim)

  dir.create(save_dir, recursive = TRUE, showWarnings = FALSE)
  saveRDS(out, file_path)

  return(out)
}

