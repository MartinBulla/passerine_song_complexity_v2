#' # Materials & Methods
#' ## Overview
#' To facilitate transparency, the Material and Methods, as well as the Results and Discussion, including all results and display items are available along with the R-code at https://martinbulla.github.io/passerine_song_complexity_v2/ [@bulla2026].
#'
#' ## Data on song complexity
#' We extracted song complexity data from song recordings available in two large citizen-science databases of bird vocalizations: [Xeno-canto](https://www.xeno-canto.org) and [Macaulay Library](The Cornell Lab of Ornithology; https://www.macaulaylibrary.org). When access to recordings of endangered or vulnerable species was restricted, we directly asked the authors to send us recordings and provide permission for their use. Also, we used only high-quality recordings, scored as quality ‘A’ or ‘B’ in Xeno-canto, or rated four or more stars in Macaulay Library.
#'
#' We focused exclusively on the recordings of a song (as indicated on the platform storing the recordings), ignoring other types of vocalizations (calls) or non-vocal sounds (e.g., produced by wing or tail feathers). Where possible, we analyzed recordings of adult male birds; else, we analyzed recordings for which the sex and age were unknown. Although female song is more common in tropical species than in temperate ones [@tobias2016 ;@odom2014a], we believe that this is unlikely to affect the patterns described here because (1) most of the recordings with solo singing presumably represent male vocalizations as females often sing in duets and solo singing by females is relatively rare [@mikula2020a; @hall2004a; @mann2003a], and (2) in species where both sexes regularly sing males and females typically produce similarly complex songs [@pilowsky2013a; @hall2009a; @mann2003a; @price2009a; @langmore1998a].
#'
#' Depending on data availability, P.M. collected 1–5 recordings for each species (median = `r round(median(d$N_recordings))`, mean = `r round(mean(d$N_recordings),1)`, N = `r as.character(sum(d$N_recordings))` recordings of `r nrow(d)` species). For most species, we only included recordings spatially separated by at least 1 km. However, for a few species with small ranges or species with poor recording coverage, we also collected recordings that did not meet this criterion. These recordings were only included if they had been obtained at least 100 meters apart in altitude or in different breeding seasons.
#'
#' Song complexity was measured using spectrograms (Fast Fourier transformation length: 256 Hz, Hamming window with 50% overlap) generated with Avisoft SASLab, version 5.2.09 (Avisoft Bioacoustics; www.avisoft.com). Recordings of the different species were analyzed in a random order and blind to species identity to exclude any observer-expectancy bias. For each recording, P.M. counted the first 50 elements and determined the number of different element types (i.e., element repertoire size) as a measure of a male’s song complexity. Thus, male’s song complexity can have values between 1 and 50. We defined an element as the smallest continuous structure on the spectrogram that is separated from another such structure by at least 10 milliseconds [@mason2014a]. Elements were inspected visually, and different element types were categorized based on element shape, frequency and duration. In some species, particularly Suboscines, a song consisted of elements with a similar shape that were repeated many times but gradually changed in frequency. We scored such songs as having two types of elements [@weir2011].
#'
#' We validated our approach by asking six volunteers (all but one untrained and never having worked in the field of bioacoustics) to score male’s song complexity in 25 sonograms (19 for Oscines, six for Suboscines; each from a different species and family). Volunteers had no information about the identity of the species being scored or about the aim of the study. The male’s song complexity score (log-transformed) was highly repeatable between the scorers (estimate [95%CI] = `r r[scale == 'log10' & type =='between observers', .(repeatability)]` [`r r[scale == 'log10' & type =='between observers', .(CI)]`]; estimated with the *rpt* function from the *rptR* package [@stoffel2017a] using Gaussian distribution and 1000 bootstrap samples. Importantly, our simple method is highly repeatable within species (`r r[scale == 'log10' & type =='within species' & complexity =='original', .(repeatability)]` [`r r[scale == 'log10' & type =='within species'& complexity =='original', .(CI)]`]) and hence allows comparing song complexity across species. The method is particularly useful for continuous singers in which it is difficult to define what constitutes a single song.
#'
#' Because not all recordings contained 50 elements, we also counted the number of scored elements for each individual recording. Our measure of male’s song complexity is inherently linked to the number of scored elements via two different processes. First, short recordings may not capture the full complexity of a male song (i.e., additional element types were missed). Second, species with simple and monotonous songs may not be recorded for long enough (here for 50 elements), because the recording person anticipated no further increase in the number of element types (i.e., no further element types would occur in such species). We could in principle account for the number of scored elements by fitting it as a covariate, yet this would confound true species differences with true within-species slopes. Alternatively, not accounting for the number of scored elements would likely underestimate male’s song complexity for species with only few scored elements. We thus applied a correction that stands between these two extremes. Out of species with 5 recordings, we selected 192 species with the largest variation (standard deviation >0.3) in the number of scored elements (log-transformed; Fig. [S7](#F_S7))). We then fitted the complexity as a dependent variable and the number of scored elements as a predictor variable (both log-transformed), species as random intercept and the number of scored elements as random slope. Using the slope of the regression (`r round(as.numeric(fixef(lmer(log10(element_types) ~ log10(elements) + (log10(elements) | scinam), data = x[scinam %in% d5$scinam[d5$elements_sd_log > 0.3]]))[2]),3)`), we corrected the male’s song complexity (i.e., number of song element types) for the recordings with < 50 scored elements by approximating the complexity as if 50 elements would have been scored:
# $$
# extrapolatedComplexity = 10^{(log(complexity) + 0.2644431*(log(50) - log(numberOFelements)))}
# $$
#' <center>
#' *extrapolated complexity = 10<sup>(log(male's song complexity) + 0.2644431\*(log(50) - log(number of scored elements)))</sup>*
#' </center>
#'
#' <br/>
#'
#' Such correction seems reasonable because species slopes were generally shallow (Fig. [S8](#F_S8)). Moreover, as expected, with the use of extrapolated song complexity, our results (hypotheses tests; Fig. [S9](#F_S9)) turned out intermediate, i.e., lie between the two extremes: results based on song complexity (i) not controlled for number of recorded elements and (ii) controlled for number of recorded elements by specifying number of recorded elements as a fixed effect within the model. Thus, in further analyses, we used the species mean of extrapolated song complexity (hereafter “song complexity”).
#'
#' Note that metrics inferring song complexity from the number of different elements and syllables are more robust to variable song structure and basic song unit definitions than other metrics such as song repertoire size and the number of unique syllables per song [@snyder2019a].

