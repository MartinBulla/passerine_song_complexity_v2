#' <a name="F_S9">**Figure S9</a> | Comparison of estimates according to the type of control for # of recorded song elements.** **None** (`r symbol(958)`) indicates that song complexity was fitted without any control. **Extrapolated** (**o**) indicates that song complexity was extrapolated as described above and the estimates reflect those from the main text Fig. [2](#F_2) (see there for model specification). **Fitting # of recorded elements** (`r symbol(950)`) indicates that song complexity was fitted along with the number of recorded elements. As expected, estimates from the model on extrapolated song complexity (**o**) lie between the estimates of song complexity not controlled for the number of recorded song elements (`r symbol(958)`) and song complexity estimates controlled for the number of recorded elements (`r symbol(950)`).
#'
#'
#' ## Breeding ranges
#' We obtained data on the breeding range of each species from ref. [@birdlife2018a] by subsetting the database to taxonomic order Passeriformes, resident species (seasonality = 1) and breeding season (seasonality = 2) and removing species introduced outside of their historical distribution range through either direct or indirect human activity (origin = 3).
#'
#' ## Phylogenetic data
#' We constructed a maximum credibility tree using the *maxCladeCred* function from the *phangorn* R-package v. 2.5.5 [@schliep2011a] and 100 randomly sampled phylogenetic trees (the Hackett backbone) from http://birdtree.org [@jetz2012a].
#'
#' ## Taxonomy
#' We used the eBird taxonomy 2022 [@sullivan2009a].
#'
#'
#' ## Predictor variables
#' **Body size and sexual size dimorphism.** Body size data, estimated as wing length, were obtained from ref. [@tobias2022a]. We then calculated sexual size dimorphism as male wing length (log-transformed) minus female wing length (log-transformed). Thus, positive values indicate species with larger males than females, suggesting more intense male−male competition and stronger sexual selection acting on males [@fairbairn2007a].
#'
#' **Plumage color and sexual color dimorphism.** We used three plumage color metrics. (1) Male-like plumage coloration (hereafter “male-like coloration”) describes the degree of male plumage elaboration based on *Handbook of the Birds of the World illustrations*, with low values indicating female-like dull plumage and high values indicating elaborate male-like plumage (retrieved from ref. [@dale2015]). (2) Male plumage colorfulness (hereafter “male colorfulness”) quantifies the diversity of male plumage colors from calibrated visible and ultraviolet photographs of museum specimens, measured as the number of occupied loci in avian tetrahedral color space (retrieved from ref. [@cooney2022]). This metric captures color diversity rather than brightness or conspicuousness, with higher values indicating a broader range of distinct plumage colors. (3) Sexual color dimorphism was calculated as the male minus female plumage coloration score [@dale2015]. The two male plumage metrics were used for different purposes. We used male colorfulness in spatial visualizations and in analyses of acoustic–visual signal partitioning because it directly quantifies male color diversity and was previously shown to vary with latitude and habitat. We used male-like coloration in the main multivariate model because it has broader taxonomic coverage and therefore maximizes species retention when testing predictors of song complexity. Nevertheless, models with male colorfulness yielded similar conclusions (Fig. [S6](#F_S6)).
#'
#' **Degree of social polygyny.** An index of the sexual selection intensity on males was retrieved from ref. [@delhey2023]. The 6-point index is based on the social mating system and parental care where zero indicates low selection on males (monogamy or polyandry, and biparental care) and five the highest selection (polygyny and uniparental female care).
#'
#' **Extra-pair paternity.** We retrieved extra-pair paternity data from ref. [@valcu2025a]. Where multiple entries were available per species, we calculated a mean value. We used two standard extra-pair paternity metrics (a) the proportion of extra-pair offspring and (b) and the proportion of extra-pair broods.
#'
#' **Territoriality and stability of social bonds.** Data were retrieved from the ref. [@tobias2016] where species is classified as 0 = non-territorial, 1 = seasonally or weakly territorial, and 2 = year-round territorial, as well as 0 = solitary, 1 = short-term pair or group bonds, and 2 = long-term pair or group bonds.
#'
#' **Migration.** Data were retrieved from ref. [@dufour2020a] where  species is assigned a score: 1 = resident (all populations occur year‐round within their breeding range ), 2 = partial migrant (the species consists of a strict migrant and resident fraction), and 3 = strict migrant (all individuals move to a wintering range geographically disjunct from the breeding one). We corrected migratory status for *Phylloscopus proregulus*, *Spizella pallida* and *Vermivora bachmanii* from “resident” in ref. [@dufour2020a] to “migratory” using information from ref. [@hoyo2020a].
#'
#' **Habitat generalism**. Data on habitat generalism were obtained from ref. [@ducatez2014a]. The index is based on patterns of species co-occurrence within each of the 101 habitat categories recognized by the IUCN (http://www.iucnredlist.org). A generalist species inhabits a range of habitats that vary considerably in species composition, whereas specialist species inhabit only habitats with a consistent set of other species.
#'
#' **Latitude**. We determined species breeding latitude as the center of the species breeding range [@birdlife2018a], i.e., the point within the breeding range that is the furthest from the edge of the breeding range.
#'
#' **Altitude**. We determined altitude as the mean breeding altitude of the species based on the species’ breeding range [@birdlife2018a] and using information from ref. [@danielson2011].
#'
#' **Species richness**. We extracted species richness at the center of the species breeding range (see Latitude above) using breeding ranges of all passerines [@birdlife2018a].
#'
#' **Tree cover**. We quantified local tree cover using the Copernicus Global Land Cover Collection 2 tree-cover fraction product (epoch 2015; EPSG:4326; nominal 100 m resolution) [@buchhorn2020]. For each recording location, we extracted, using *exactextractr* [@baston2025], the mean percentage tree cover of all raster pixels intersecting a circular buffer of radius 0.001° (~100 m at the equator), excluding missing values (ocean/nodata). For locations with no valid pixels at this scale (n = 153), we repeated the extraction using a larger buffer (0.005°, ~500 m). Locations that could not be assigned to a raster tile (n = 26) or had no valid pixels even at 0.005° (n = 52) were treated as missing for tree-cover analyses. Species-level tree cover was calculated as the mean across all available recording locations and correlates with discrete habitat density [@tobias2022a] and forest dependency [@cooney2022] (Fig. [S10](#F_S10)].
#'
#' **Brain size**. Data on brain size were taken from ref. [@sayol2018] along with the corresponding body mass. We corrected the body mass for *Acanthiza ewingii* from 4g reported in ref. [@sayol2018] to 7g (using information from ref. [@hoyo2020a]) and calculated residual brain size as log brain size given log body mass controlled for taxonomy random intercepts. In birds, absolute and relative brain size are positively correlated with the number of pallial neurons, which correlate with cross-species differences in innovativeness [@sol2022a].
#'
#'
#' ## Statistical analyses
#' All statistical analyses were performed using R v. 4.2.5 [@team2025]. If not stated otherwise, comparative analyses were performed using taxonomic mixed-effect models fitted with the *lmer* function from the *lme4* package [@gelman2007a]. We then used the *sim* function from the *arm* package and a non-informative prior distribution [@gelman2022; @gelman2007a] tto create a sample of 5000 simulated values for each model parameter (i.e., posterior distribution). Continuous variables were z-transformed, i.e., mean-centered and divided by standard deviation [@schielzeth2010]. We report effect sizes and model predictions by the medians, and the uncertainty of the estimates and predictions by the Bayesian 95% credible intervals represented by the 2.5 and 97.5 percentiles (i.e., 95% CI) from the posterior distribution of 5000 simulated or predicted values. We graphically inspected the goodness of fit, and the distribution of the residuals (see ref. [@bulla2026]). To explore and describe variation in song complexity across passerines, we first visualized the geographic distribution of song complexity and its predictors, using the passerine breeding ranges and the *rangeMapper* package [@valcu2012]. Specifically, a *rangeMapper* project was set up using hexagonal cells [@birch2007] with a diagonal of 130 km on a pseudo-cylindrical, equal-area projection (Eckert IV). For each grid cell, we calculated mean song complexity (and predictor) values for all species occurring within each cell (assemblage), as well as mean song complexity values for only Oscines or Suboscines. Second, we reconstructed ancestral song complexity states using the maximum-likelihood approach implemented in the *contMap* function in the *phytools* package [@revell2012]. We then mapped character states on a maximum credibility tree.
#'
#' To investigate phylogenetic signal, i.e., Pagel’s lambda (λ), in song complexity, we used phylogenetic generalized least-squares (PGLS) regressions with Pagel’s lambda (λ) transformation of a correlation structure [@pagel1999] and the maximum credibility tree. This method explicitly models how the covariance between species declines as they become more distantly related, and simultaneously estimates the regression parameters [@revell2010]. If λ = 1, modelled traits co-vary in direct proportion to shared evolutionary history, whereas λ = 0 indicates phylogenetic independence of the model’s residuals [@freckleton2002]. We fitted such phylogenetic models with the *phylolm* function from the *phylolm* package [@tung2014].
#'
#' To investigate the source of phylogenetic constraint in song complexity, we estimated the proportion of phenotypic variance explained by various taxonomic levels [@bulla2016]. Because clade is a two-level factor (Oscines or Suboscines), we first fitted an intercept only mixed effect model with genus, family, superfamily, parvorder and infraorder as nested random intercepts to estimate the overall phenotypic variance. We then entered clade as a fixed factor and estimated the variance explained by clade as the difference between the total variance (random and residual) from the first and the second model. To evaluate the proportion of the variance explained by genus, family, superfamily, parvorder and infraorder, we used the estimates from the model that included clade. Note that in this and other analyses we lumped the clade Acanthisitti (here represented by a single species) with the Suboscines as song in both clades is largely innate.
#'
#' To test for the drivers of song complexity, we specified taxonomic models with clade (two-level factor: Oscines or Suboscines) as a fixed effect, and genus, family, superfamily, parvorder and infraorder, as random intercepts. A multivariate model included all continuous predictors in interaction with clade whereas univariate models included only a single continuous predictor in interaction with clade. The multicollinearity between predictors was low, all |r| < 0.64 (Fig. [S11](#F_S11)). Song complexity and altitude were log-transformed before analysis [@schielzeth2010].  As an alternative way of controlling for non-independence due to common ancestry [@paradis2006], we also specified multivariate phylogenetic generalized least-squares model containing all predictors in interaction with clade and maximum credibility tree to specify the expected covariance structure among species (for details see the above section on phylogenetic signal).
#'
#' To investigate whether song complexity and male colorfulness show context-dependent associations in Oscines, we fitted an additional taxonomic model with song complexity as the dependent variable and the three-way interaction between male colorfulness, tree cover, and latitude as predictors.
#'
#' To post-hoc explore whether the effects of habitat generalism and migration on song complexity were interactive, we fitted an additional taxonomic model with three-way interaction of clade, habitat generalism and migration.
#'
#' To investigate whether song complexity was associated with brain mass, we fitted taxonomic models with clade interacting with either absolute brain mass (in grams; log-transformed) or residual brain mass (i.e., after accounting for body mass). In addition, we updated these models by adding a clade–migration interaction. Furthermore, we investigated whether brain mass, absolute or residual, was associated with migration by fitting taxonomic models with brain mass as the response variable and clade, migration and their interaction as predictors. Because migratory strategy was associated with both song complexity and residual brain mass, we further assessed whether their apparent relationship persisted after accounting for migration and nested taxonomic structure. To do so, we extracted conditional residuals from two separate taxonomic models, one for song complexity and one for residual brain mass, both with migration as the predictor. We then fitted a taxonomic model with song-complexity residuals as the response and clade in interaction with residual-brain-mass residuals as predictors.
#'
#' For system information and used R-packages and along with their versions, see Table [S11](#T_S11) and [S12](#T_S12) .
#'
