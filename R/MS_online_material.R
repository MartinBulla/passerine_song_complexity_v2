#'
#' ***
#'
#' Three main hypotheses have been proposed to explain the evolution of song complexity.
#'
#' **(1) ‘Intensity of sexual selection’ hypothesis** – Strong intra- and inter-sexual selection has been proposed to favor males with complex songs [@nowicki2004a; @catchpole1987a; @searcy1986]. However, we find no strong evidence that sexual selection predicts variation in passerine song complexity (Fig. [3](#F_3)), based on multiple traditional proxies for sexual selection intensity: sexual size dimorphism [@dale2007; @dunn2001a], sexual plumage-color dimorphism [@dale2015], male-like coloration, reflecting similarity between female and male plumage [@mason2014a; @dale2015; @cooney2022], degree of social polygyny  [@snyder2019a; @shutler1990a], territoriality (i.e., the intensity of sexual selection on males) [@lyon2012a; @tobias2016], and stability of social bonds (i.e., opportunity for mate choice) [@lyon2012a; @tobias2016]. Consistent with this result, song complexity is also unrelated to the level of extra-pair paternity (Table [S4](#T_S4)). A partial exception is observed in Suboscines, where territorial species show a noisy trend toward lower song complexity than non-territorial species (Fig. [3](#F_3); see also [@scordato2018a]).
#'
#' **(2) ‘Species recognition’ hypothesis** – Reduced song complexity has been predicted in areas with high numbers of breeding passerine species, since overly complex songs may hinder rapid and efficient species recognition [@silva2022a; @singh2015a]. However, we find no support for such a negative association between species richness and song complexity (Fig. [3](#F_3) and [S3](#F_S3)).
#'
#' **(3) ‘Acoustic adaptation’ hypothesis** – Dense habitats are expected to select for structurally simple acoustic signals because sounds degrade during transmission via absorption, reverberation, and scattering, processes typically stronger in closed, forested habitats [@morton1975a; @wiley1982a; @wiley1978a; @crouch2019]. Indeed, in song-learning Oscines, song complexity is lower in forests than in open habitats (Fig. [3](#F_3) and [S3](#F_S3)), consistently across latitudes (i.e., not restricted to tropical or temperate forests; Table [S5](#T_S5)). This tree cover effect was not observed for peak song frequency [@mikula2021a].
#'
#' The habitat axis that predicts song complexity also structures visual signals: male colorfulness increases with forest-dependent lifestyle and toward lower latitudes [@cooney2022]. We therefore examined whether the association between song complexity and male colorfulness depends on ecological context. In Oscines, song complexity was negatively associated with male colorfulness only in temperate forests; this association was absent in tropical forests, open habitats, and Suboscines (Fig. [4](#F_4) and Table [S6](#T_S6)). Thus, the apparent negative song-color relationship (Fig. [2A](#F_2)). Why such context-dependent partitioning between acoustic and visual signals appears most evident in temperate forests remains unclear, perhaps because acoustic constraints, though present, are less extreme than in tropical forests [@hart2015a; @deconto2024a].
