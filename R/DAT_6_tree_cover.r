  #x <- fread("./DATA/song_raw_prepared.csv")
  xx = x[!is.na(latit) & !is.na(longit)]
  xx[, lat := latit]
  xx[, lon := longit]

# GET: bounding box of each raster tile
  tree_cover_loc = '/Users/martinbulla/Dropbox/Science/DATA_collection/comparative/remote_sensing/Copernicus_Land_cover/data_source/tree_cover' # set the location of your tiles; the tiles are names as follows 'E000N00_ProbaV_LC100_epoch2015_global_v2.0.1_tree-coverfraction-layer_EPSG-4326.tif'

  squares =  data.table( fn = list.files(tree_cover_loc, full.names = TRUE) )
  squares[, i := .I]

  squares[, bnd := raster(fn) %>% extent %>% as(., 'SpatialPolygons') %>% st_as_sf , by = i]
  squares = st_as_sf(squares)  %>% st_buffer(dist = 0)

# ASSIGN: raster tile ID to each observation
  xs = st_intersection(st_as_sf(xx, coords = c('longit', 'latit') ), squares)
  setDT(xs)

# EXTRACT: % tree cover

## PASS 1 - extraction within ~100m buffer

### cluster
cl <- makeCluster(parallel::detectCores() - 7)
registerDoSNOW(cl)

### progress bar
pb <- txtProgressBar(min = 0, max = nrow(xs), style = 3)
progress <- function(n) setTxtProgressBar(pb, n)
opts <- list(progress = progress)

### loop
o1 <- foreach(i = seq_len(nrow(xs)), .combine = rbind,
              .packages = c("raster","sf","units","exactextractr","data.table"),
              .options.snow = opts, .errorhandling = "pass") %dopar% {

  xi  <- xs[i, ]
  ri  <- raster(xi$fn)

  spi <- st_as_sf(xi$geometry, crs = projection(ri)) %>%
    st_buffer(dist = set_units(0.001, "degree")) # 0.001 ~ 100-110m

  oi <- exactextractr::exact_extract(ri, spi)[[1]]
  np <- sum(!is.na(oi$value))
  val <- if (np == 0) NA_real_ else mean(oi$value, na.rm = TRUE)

  data.table(id = xi$id,
             perc_forest = val,
             npixels = np,
             radius_deg = if (np == 0) NA_real_ else 0.001)
}
close(pb)
stopCluster(cl)

### IDs to rescue
