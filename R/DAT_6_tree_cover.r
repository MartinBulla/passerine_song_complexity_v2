## PASS 2 - adaptive buffer only for the 153 recordings
### helper: adaptive extraction within one tile
extract_tc_adaptive <- function(ri, geom, radii_deg = c(0.001, 0.002, 0.005)) {
  for (d in radii_deg) {
    spi <- st_as_sf(geom, crs = projection(ri)) %>%
      st_buffer(dist = set_units(d, "degree"))

    oi <- exactextractr::exact_extract(ri, spi)[[1]]
    np <- sum(!is.na(oi$value))

    if (np > 0) {
      return(list(val = mean(oi$value, na.rm = TRUE), npixels = np, radius_deg = d))
    }
  }
  list(val = NA_real_, npixels = 0L, radius_deg = NA_real_)
}

### subset xs to only problematic IDs
xs2 <- xs[xs$id %in% ids0, ]
xs2 <- xs2[match(ids0, xs2$id), ]  # keep stable order by ids0

### cluster
cl <- makeCluster(parallel::detectCores() - 7)
registerDoSNOW(cl)

### progress bar
pb <- txtProgressBar(min = 0, max = nrow(xs2), style = 3)
progress <- function(n) setTxtProgressBar(pb, n)
opts <- list(progress = progress)

### loop
o2 <- foreach(i = seq_len(nrow(xs2)), .combine = rbind,
              .packages = c("raster","sf","units","exactextractr","data.table"),
              .options.snow = opts, .errorhandling = "pass") %dopar% {

  xi <- xs2[i, ]
  ri <- raster(xi$fn)

  res <- extract_tc_adaptive(ri, xi$geometry,
                            radii_deg = c(0.001, 0.002, 0.005, 0.02))  # choose your cap(s)

  data.table(id = xi$id,
             perc_forest = res$val,
             npixels = res$npixels,
             radius_deg = res$radius_deg)
}
close(pb)
stopCluster(cl)

